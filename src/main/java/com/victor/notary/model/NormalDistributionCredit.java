package com.victor.notary.model;

public class NormalDistributionCredit {
    private Integer id;

    private Float begincredit;

    private Float endcredit;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Float getBegincredit() {
        return begincredit;
    }

    public void setBegincredit(Float begincredit) {
        this.begincredit = begincredit;
    }

    public Float getEndcredit() {
        return endcredit;
    }

    public void setEndcredit(Float endcredit) {
        this.endcredit = endcredit;
    }
}